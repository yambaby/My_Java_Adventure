package JAVA;

public class Loops {
    public static void main(String[] args){
        for (int i = 1; i <= 10; i++){
            System.out.println("Hello" + i);
        }

        for (int j = 5; j > 0; j--){
            System.out.println("Hello" + j);
        }

        int i = 0;
        while (i == 0){
            System.out.println("hello");
            i++;
        }

    }

}
